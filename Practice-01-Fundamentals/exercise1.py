# Exercício 1: No terminal, inicialize duas variáveis a e b,
# sendo a = 10 e b = 5. Mostre o resultado das 7 operações básicas
# (soma, subtração, multiplicação, divisão, divisão inteira,
# potenciação e módulo) envolvendo essas variáveis.

a = 20
b = 5

print(a + b, '<<<< a + b')
print(a - b, '<<<< a - b')
print(a / b, '<<<< a / b')
print(a * b, '<<<< a * b')
# shows the rest of division
print(a % b, '<<<< a % b - rest of division')
# shows the integer of division
print(a // b, '<<<< a // b - integer of division')
print(a ** b, '<<<< a ^ b')
